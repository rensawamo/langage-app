/// AppIconButtonのレイアウトタイプ
enum AppColorType {
  // default  ダークモードなら 白 ライトモードなら黒
  defaultColor,
  // dafautcolor の逆 ダークなら 黒 ライトなら白
  reverseColor,
  // Appbarの色
  appbar,
  // 背景の色
  background,
  // ヘッダーのテキストの色
  headerText,
  // 影
  shadow,
  // タイトル文字の色
  titleCharacter,
  //  ページのタイトルの色
  pageTitle,
  // クイズの次へのボタンの色
  nextButton,
  // 問題に正解したときの色
  collectAnswer,
  // 問題に不正解したときの色
  wrongAnswer,
  // ラジオボタンがonの時
  radioOn,
  // ラジオボタンがoffの時
  radioOff,
  // setting のtileのタップ時のエフェクトの色
  effectColor,

}
